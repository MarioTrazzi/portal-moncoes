const SelectContent = React.forwardRef<
  React.ElementRef<typeof SelectPrimitive.Content>,
  React.ComponentPropsWithoutRef<typeof SelectPrimitive.Content>
>(({ className, children, position = "popper", ...props }, ref) => {
  const { theme, resolvedTheme } = useTheme()
  
  // Use useEffect para aplicar classes e estilos ao portal após renderização
  React.useEffect(() => {
    const applyStyles = () => {
      const portals = document.querySelectorAll('[data-radix-select-content]')
      const currentTheme = resolvedTheme || theme
      
      portals.forEach(portal => {
        portal.classList.remove('light', 'dark')
        if (currentTheme) {
          portal.classList.add(currentTheme)
        }
        
        // Forçar estilos inline como fallback
        const element = portal as HTMLElement
        if (currentTheme === 'dark') {
          element.style.setProperty('background-color', '#020617', 'important')
          element.style.setProperty('color', '#f8fafc', 'important')
          element.style.setProperty('border-color', '#334155', 'important')
        } else {
          element.style.setProperty('background-color', '#ffffff', 'important')
          element.style.setProperty('color', '#0f172a', 'important')
          element.style.setProperty('border-color', '#e2e8f0', 'important')
        }
        element.style.setProperty('box-shadow', '0 10px 15px -3px rgba(0, 0, 0, 0.1), 0 4px 6px -2px rgba(0, 0, 0, 0.05)', 'important')
        element.style.setProperty('opacity', '1', 'important')
        element.style.setProperty('visibility', 'visible', 'important')
      })
    }
    
    // Aplicar imediatamente
    applyStyles()
    
    // Aplicar novamente após um pequeno delay para garantir
    const timeout = setTimeout(applyStyles, 100)
    
    return () => clearTimeout(timeout)
  }, [theme, resolvedTheme])
  
  return (
    <SelectPrimitive.Portal>
      <SelectPrimitive.Content
        ref={ref}
        className={cn(
          "relative z-50 max-h-96 min-w-[8rem] overflow-hidden rounded-md border shadow-md data-[state=open]:animate-in data-[state=closed]:animate-out data-[state=closed]:fade-out-0 data-[state=open]:fade-in-0 data-[state=closed]:zoom-out-95 data-[state=open]:zoom-in-95 data-[side=bottom]:slide-in-from-top-2 data-[side=left]:slide-in-from-right-2 data-[side=right]:slide-in-from-left-2 data-[side=top]:slide-in-from-bottom-2",
          position === "popper" &&
            "data-[side=bottom]:translate-y-1 data-[side=left]:-translate-x-1 data-[side=right]:translate-x-1 data-[side=top]:-translate-y-1",
          className
        )}
        position={position}
        style={{
          backgroundColor: theme === 'dark' ? '#020617' : '#ffffff',
          color: theme === 'dark' ? '#f8fafc' : '#0f172a',
          borderColor: theme === 'dark' ? '#334155' : '#e2e8f0',
        }}
        {...props}
      >
        <SelectScrollUpButton />
        <SelectPrimitive.Viewport
          className={cn(
            "p-1",
            position === "popper" &&
              "h-[var(--radix-select-trigger-height)] w-full min-w-[var(--radix-select-trigger-width)]"
          )}
        >
          {children}
        </SelectPrimitive.Viewport>
        <SelectScrollDownButton />
      </SelectPrimitive.Content>
    </SelectPrimitive.Portal>
  )
})
SelectContent.displayName = SelectPrimitive.Content.displayName

